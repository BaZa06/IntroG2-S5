díadesemana = int(input("Introduce un número del 1 al 7: "))
if díadesemana == 1:
    print("Lunes")
elif díadesemana == 2:
    print("Martes")
elif díadesemana == 3:
    print("Miércoles")
elif díadesemana == 4:
    print("Jueves")
elif díadesemana == 5:
    print("Viernes")
elif díadesemana == 6:
    print("Sábado")
elif díadesemana == 7:
    print("Domingo")
else:
    print("Se produjo un error")